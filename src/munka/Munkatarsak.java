    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // Ha a felhasználó kiválaszt egy sort
        int sor = jTable1.getSelectedRow();
        
        //kigyűjtöm a sorból az adatokat
        int id = Integer.parseInt(jTable1.getModel().getValueAt(sor, 0).toString());
        String teljesnev = jTable1.getModel().getValueAt(sor, 1).toString();
        String taj = jTable1.getModel().getValueAt(sor, 2).toString();
        String adoszam = jTable1.getModel().getValueAt(sor, 3).toString();
        String anyja = jTable1.getModel().getValueAt(sor, 4).toString();
        
        txAzon.setText(Integer.toString(id));
        txNev.setText(teljesnev);
        txTaj.setText(taj);
        txAdoaz.setText(adoszam);
        txAnyja.setText(anyja);
        
        //munkaidő információk kiolvasása
        jTextArea1.setText("");
        try 
        {
            Connection con = DriverManager.getConnection(host, user, password);
            String sql = "SELECT * FROM vizsga_munkaido WHERE munkatarsID="+id;
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            int tulora = 0;
            while(rs.next())
            {
                jTextArea1.append(rs.getString("mettol") + "-" + rs.getString("meddig") + "\n");
                tulora += rs.getInt("tulora");
            }
            jTextArea1.append(Integer.toString(tulora));
        } 
        catch (Exception e) 
        {
            
        }
    }//GEN-LAST:event_jTable1MouseClicked

