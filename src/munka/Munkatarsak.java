    private void tableFrissites()
    {
        DefaultTableModel tabla = (DefaultTableModel)jTable1.getModel();
        int sorokSzama = tabla.getRowCount();
        
        //a tábla sorait törlöm
        for(int i = sorokSzama -1; i >= 0; i--)
        {
            tabla.removeRow(i);
        }
        
        try 
        {
            Connection con = DriverManager.getConnection(host, user, password);
            String sql = "SELECT * FROM vizsga_munkatars";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next())
            {
                tabla.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("teljesnev"),
                rs.getString("taj"),
                rs.getString("adoszam"),
                rs.getString("anyjanev")});
            }
            st.close();
            con.close();
        } 
        catch (Exception e) 
        {
            System.out.println("Hiba a lekérdezéskor" + e);
        }
    }
    
